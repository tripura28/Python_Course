#difference between module,library and package
#module
'''
A module in python is a single python file it consists python code
It typically consist of functions, classes and variables that can be used in other python scripts or programs
Examples of modules include math.py, random.py or mymodule.py
'''

#package
'''
A package in python is a directory containing one or more python modules and an __init__.py file
The __init__.py file can be empty or contain initialization code for the package
Examples of packages include numpy,pandas or django
'''

#library
'''
Libraries can consist of multiple modules and packages, oraganized to serve a particular purpose or domain
Examples of libraries such as requests,numpy,pandas and matplotlib
'''

